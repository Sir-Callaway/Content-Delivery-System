		</div></div><div class="intro component" id="comp-2" style=""><p>
	The Kenya Conservatoire of Music exists to promote the teaching and performance of quality music of all styles in Kenya. Equipped with a teaching staff of 35 highly qualified and committed individuals, both native and expatriate, the Conservatoire's main focus is to ensure that every interested individual has an opportunity to experience the benefits of learning to play/sing music at an affordable cost.  The Conservatoire ensures that the students not only receive first-class, quality training in music, but more importantly that they enjoy the practice and challenges of making music. The lessons are given both at the main branch at  the Kenya Cultural Centre, at a Satellite Branch at the Go Down Arts Centre and in various schools that some of the teachers visit weekly. Every year, training is offered  to an average of 400 students.</p>
		<p>In terms of assessment, the Conservatoire has introduced a number of Diploma options for selected subjects such as Conducting, Teaching and Performance. In addition, the Conservatoire is the Kenyan representative for the Practical Examinations of the Associated Board of the Royal Schools of Music (ABRSM). The Director of The Conservatoire oversees the administration of all practical examinations. Over 1500 candidates enroll for these international examinations in various instruments each year.</p>
